public class A4Loops {
    public static void main(String[] args) {
        //    While loop
        //    number till 10.
        int count = 1;

        //While Loop
        while (count != 10) {
            System.out.println(count);
            count++;
        }

        //For Loop
        for(count=1; count != 10; count++){
            System.out.println(count);
        }

        // DO-While Loop
        do{
            System.out.println(count);
            count++;
        }
        while(count !=10);
    }
}
